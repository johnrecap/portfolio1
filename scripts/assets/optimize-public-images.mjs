import { mkdir, readdir, stat, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import sharp from 'sharp';

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..', '..');
const inputDir = path.join(rootDir, 'public', 'demo-previews');
const outputDir = path.join(inputDir, 'optimized');
const manifestPath = path.join(rootDir, 'src', 'lib', 'generated', 'demo-preview-images.ts');
const widths = [640, 960, 1280];
const supportedExtensions = new Set(['.jpg', '.jpeg', '.png', '.webp']);

function toPublicPath(filePath) {
  return `/${path.relative(path.join(rootDir, 'public'), filePath).replace(/\\/g, '/')}`;
}

function formatBytes(value) {
  if (value < 1024) {
    return `${value} B`;
  }

  return `${(value / 1024).toFixed(1)} KB`;
}

function createSrcSet(items) {
  return items.map((item) => `${item.src} ${item.width}w`).join(', ');
}

function createImageEntry(sourcePath, metadata, variants) {
  const webpVariants = variants.filter((item) => item.format === 'webp');
  const avifVariants = variants.filter((item) => item.format === 'avif');
  const fallback = webpVariants.find((item) => item.width === 960) ?? webpVariants.at(-1);

  return {
    src: fallback?.src ?? toPublicPath(sourcePath),
    fallbackSrc: toPublicPath(sourcePath),
    width: fallback?.width ?? metadata.width,
    height: fallback?.height ?? metadata.height,
    originalWidth: metadata.width,
    originalHeight: metadata.height,
    sources: [
      {
        type: 'image/avif',
        srcSet: createSrcSet(avifVariants),
      },
      {
        type: 'image/webp',
        srcSet: createSrcSet(webpVariants),
      },
    ].filter((item) => item.srcSet),
  };
}

async function optimizeImage(fileName) {
  const sourcePath = path.join(inputDir, fileName);
  const sourceStats = await stat(sourcePath);
  const source = sharp(sourcePath);
  const metadata = await source.metadata();

  if (!metadata.width || !metadata.height) {
    throw new Error(`Unable to read image dimensions for ${fileName}`);
  }

  const targetWidths = widths.filter((width) => width <= metadata.width);
  const baseName = path.basename(fileName, path.extname(fileName));
  const variants = [];

  for (const width of targetWidths) {
    const resizedHeight = Math.round((metadata.height / metadata.width) * width);

    for (const format of ['avif', 'webp']) {
      const outputPath = path.join(outputDir, `${baseName}-${width}.${format}`);
      const pipeline = sharp(sourcePath).resize({
        width,
        withoutEnlargement: true,
      });

      if (format === 'avif') {
        await pipeline.avif({ quality: 45, effort: 6 }).toFile(outputPath);
      } else {
        await pipeline.webp({ quality: 72, effort: 5 }).toFile(outputPath);
      }

      const outputStats = await stat(outputPath);
      variants.push({
        format,
        src: toPublicPath(outputPath),
        width,
        height: resizedHeight,
        bytes: outputStats.size,
      });
    }
  }

  const smallestFallback = variants
    .filter((item) => item.format === 'webp')
    .sort((left, right) => left.width - right.width)[0];
  const largestFallback = variants
    .filter((item) => item.format === 'webp')
    .sort((left, right) => right.width - left.width)[0];

  console.log(
    [
      fileName,
      `${metadata.width}x${metadata.height}`,
      `original ${formatBytes(sourceStats.size)}`,
      smallestFallback ? `webp ${smallestFallback.width}w ${formatBytes(smallestFallback.bytes)}` : '',
      largestFallback ? `webp ${largestFallback.width}w ${formatBytes(largestFallback.bytes)}` : '',
    ]
      .filter(Boolean)
      .join(' | '),
  );

  return [toPublicPath(sourcePath), createImageEntry(sourcePath, metadata, variants)];
}

function createManifestSource(entries) {
  const manifest = Object.fromEntries(entries);

  return `// Generated by scripts/assets/optimize-public-images.mjs. Do not edit by hand.

export type GeneratedImageSource = {
  type: 'image/avif' | 'image/webp';
  srcSet: string;
};

export type GeneratedImageEntry = {
  src: string;
  fallbackSrc: string;
  width: number;
  height: number;
  originalWidth: number;
  originalHeight: number;
  sources: readonly GeneratedImageSource[];
};

export const DEMO_PREVIEW_IMAGE_MANIFEST = ${JSON.stringify(manifest, null, 2)} as const satisfies Record<string, GeneratedImageEntry>;
`;
}

async function main() {
  await mkdir(outputDir, { recursive: true });
  await mkdir(path.dirname(manifestPath), { recursive: true });

  const files = (await readdir(inputDir))
    .filter((fileName) => supportedExtensions.has(path.extname(fileName).toLowerCase()))
    .sort((left, right) => left.localeCompare(right));

  if (files.length === 0) {
    throw new Error(`No supported images found in ${path.relative(rootDir, inputDir)}`);
  }

  const entries = [];
  for (const fileName of files) {
    entries.push(await optimizeImage(fileName));
  }

  await writeFile(manifestPath, createManifestSource(entries), 'utf8');
  console.log(`Manifest written to ${path.relative(rootDir, manifestPath)}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
